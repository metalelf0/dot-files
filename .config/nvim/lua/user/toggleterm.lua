local status_ok, toggleterm = pcall(require, "toggleterm")
if not status_ok then
	return
end

toggleterm.setup({
	size = 20,
	open_mapping = [[<c-\>]],
	hide_numbers = true,
	-- shade_filetypes = {},
	-- shade_terminals = false,
	-- shading_factor = 1,
	start_in_insert = true,
	insert_mappings = true,
	persist_size = true,
	direction = "float",
	close_on_exit = true,
	shell = vim.o.shell,
	float_opts = {
		border = "curved",
		highlights = {
			border = "FloatBorder",
			background = "Normal",
		},
	},
})

function _G.set_terminal_keymaps()
  local opts = {noremap = true}
  vim.api.nvim_buf_set_keymap(0, 't', '<esc><esc>', [[<C-\><C-n>]], opts)
  vim.api.nvim_buf_set_keymap(0, 't', 'jk', [[<C-\><C-n>]], opts)
end

vim.api.nvim_create_autocmd('TermOpen', { callback = function() set_terminal_keymaps() end, pattern = 'term://*' })

local Terminal = require("toggleterm.terminal").Terminal

function _LAZYGIT_TOGGLE()
  local lazygit = Terminal:new({ cmd = "lazygit", dir = vim.fn.getcwd(), hidden = true })
	lazygit:toggle()
end

